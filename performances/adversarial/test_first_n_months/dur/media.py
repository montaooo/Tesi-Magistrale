def average_dicts(dicts):
    dfs = [pd.DataFrame(d) for d in dicts]
    # Dates are all same, take it from first
    dates = dicts[0]['Date']
    # Calculate mean of numeric columns
    numeric_cols = ['Precision', 'F1', 'TNR', 'TPR']
    sum_df = pd.concat(dfs)
    mean_df = sum_df.groupby('Date', sort=False).mean().reindex(dates).round(3)
    
    res = {'Date': dates}
    for col in numeric_cols:
        res[col] = [float(x) if pd.notna(x) else float("nan") for x in mean_df[col]]
    return res

